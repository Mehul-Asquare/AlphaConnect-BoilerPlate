const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');
const upload = require('../../middlewares/multer.config');
const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), validate(userValidation.createUser), userController.createUser)
  .get(auth('getUsers'), validate(userValidation.getUsers), userController.getUsers);
router
  .route('/:userId')
  .get(auth('getUsers'), validate(userValidation.getUser), userController.getUser)
  .patch(auth('manageUsers'), validate(userValidation.updateUser), upload.single('image'), userController.updateUser)
  .delete(auth('manageUsers'), validate(userValidation.deleteUser), userController.deleteUser);
router
  .route('/cover-image/:userId')
  .patch(auth('manageUsers'), validate(userValidation.updateUserCoverImage), upload.single('coverImage'), userController.uploadCoverImage)
  .delete(auth('manageUsers'), validate(userValidation.deleteUserCoverImage), userController.deleteCoverImage);
router
  .route('/social-media/:userId') // Use a suitable route path
  .patch(auth('manageUsers'), validate(userValidation.createOrUpdateSocialMedia), userController.createOrUpdateSocialMedia);
router
  .route('/company/:userId')
  .post(auth('manageUsers'), validate(userValidation.addCompany), upload.single('company_image'), userController.addCompany)
  .delete(auth('manageUsers'), validate(userValidation.deleteCompanyImage), userController.deleteCompanyImage);
router
  .route('/company/:userId/:companyId') // Use the appropriate route path
  .patch(auth('manageUsers'), validate(userValidation.updateCompanyDetail), upload.single('company_image'), userController.updateCompanyDetail)
  .delete(auth('manageUsers'), validate(userValidation.deleteCompanyDetail), userController.deleteCompanyDetail);
router
  .route('/office-timing/:userId')
  .post(auth('manageUsers'), validate(userValidation.addOfficeTiming), userController.addOfficeTiming)
  .get(auth('getUsers'), validate(userValidation.getUser), userController.getOfficeTimings);
router
  .route('/gallery/:userId')
  .post(auth('manageUsers'), validate(userValidation.uploadGalleryImages), upload.array('images', 10), userController.uploadGalleryImages)
  .delete(auth('manageUsers'), validate(userValidation.deleteGalleryImages), userController.deleteGalleryImages);
router
  .route('/files/:userId')
  .post(auth('manageUsers'), validate(userValidation.uploadFiles), upload.array('documents', 5), userController.uploadFiles);
router
  .route('/product/:userId')
  .post(auth('manageUsers'), validate(userValidation.createProduct), upload.fields([{ name: 'files', maxCount: 2 }, { name: 'images', maxCount: 5 }]), userController.createProduct);
router
  .route('/product/:userId/:productId')
  .patch(auth('manageUsers'), validate(userValidation.updateProduct), upload.fields([{ name: 'files', maxCount: 2 }, { name: 'images', maxCount: 5 }]), userController.updateProduct)
  .delete(auth('manageUsers'), validate(userValidation.deleteProduct), userController.deleteProduct);
router
  .route('/image/:userId')
  .delete(auth('manageUsers'), validate(userValidation.deleteUserImage), userController.deleteImage);

module.exports = router;

